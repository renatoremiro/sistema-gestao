class MigracaoSupabase {
    constructor() {
        this.firebaseData = null;
        this.supabaseClient = null;
        this.logMigracao = [];
        this.status = 'aguardando';
        
        console.log('üîÑ M√≥dulo de Migra√ß√£o Supabase carregado');
    }

    // üìä INICIALIZAR MIGRA√á√ÉO
    async inicializar() {
        try {
            console.log('üöÄ Inicializando migra√ß√£o Firebase ‚Üí Supabase...');
            this.status = 'inicializando';

            // Verificar se Supabase est√° dispon√≠vel
            if (!window.supabaseClient) {
                throw new Error('Supabase client n√£o encontrado');
            }
            this.supabaseClient = window.supabaseClient;

            // Testar conex√£o Supabase
            const conectado = await this.supabaseClient.testarConexao();
            if (!conectado) {
                throw new Error('Falha na conex√£o com Supabase');
            }

            this.log('‚úÖ Migra√ß√£o inicializada com sucesso');
            this.status = 'pronto';
            return true;

        } catch (error) {
            this.log(`‚ùå Erro na inicializa√ß√£o: ${error.message}`);
            this.status = 'erro';
            return false;
        }
    }

    // üõ°Ô∏è BACKUP COMPLETO FIREBASE
    async backupFirebase() {
        try {
            console.log('üõ°Ô∏è Iniciando backup completo do Firebase...');
            this.status = 'backup';

            const backup = {
                timestamp: Date.now(),
                data: new Date().toISOString(),
                versao: '8.13.0',
                dados: {}
            };

            // Aguardar Firebase estar pronto
            if (window.firebaseInitPromise) {
                await window.firebaseInitPromise;
            }

            if (!window.database) {
                throw new Error('Firebase n√£o inicializado');
            }

            // Backup dos dados principais
            const tabelas = [
                'usuarios', 
                'eventos', 
                'tarefas', 
                'participantes',
                'configuracoes'
            ];

            for (const tabela of tabelas) {
                try {
                    const snapshot = await window.database.ref(tabela).once('value');
                    backup.dados[tabela] = snapshot.val() || {};
                    this.log(`üì• Backup ${tabela}: ${Object.keys(backup.dados[tabela]).length} registros`);
                } catch (error) {
                    this.log(`‚ö†Ô∏è Erro no backup ${tabela}: ${error.message}`);
                    backup.dados[tabela] = {};
                }
            }

            // Salvar backup local
            localStorage.setItem('backup_firebase_migration', JSON.stringify(backup));
            
            this.firebaseData = backup;
            this.log('‚úÖ Backup Firebase completo realizado');
            
            // Download do backup
            this.downloadBackup(backup);
            
            return backup;

        } catch (error) {
            this.log(`‚ùå Erro no backup Firebase: ${error.message}`);
            throw error;
        }
    }

    // üíæ DOWNLOAD BACKUP
    downloadBackup(backup) {
        try {
            const dataStr = JSON.stringify(backup, null, 2);
            const dataBlob = new Blob([dataStr], { type: 'application/json' });
            
            const link = document.createElement('a');
            link.href = URL.createObjectURL(dataBlob);
            link.download = `backup-firebase-${new Date().toISOString().split('T')[0]}.json`;
            link.click();
            
            this.log('üíæ Backup baixado com sucesso');
        } catch (error) {
            this.log(`‚ö†Ô∏è Erro no download: ${error.message}`);
        }
    }

    // üîÑ MIGRAR USU√ÅRIOS
    async migrarUsuarios() {
        try {
            this.log('üë• Migrando usu√°rios...');
            
            if (!this.firebaseData?.dados?.usuarios) {
                this.log('‚ö†Ô∏è Nenhum usu√°rio encontrado no Firebase');
                return { sucesso: 0, erro: 0 };
            }

            const usuarios = this.firebaseData.dados.usuarios;
            let sucesso = 0, erro = 0;

            for (const [id, usuario] of Object.entries(usuarios)) {
                try {
                    if (!usuario.email) {
                        this.log(`‚ö†Ô∏è Usu√°rio ${id} sem email - ignorado`);
                        continue;
                    }

                    const usuarioSupabase = {
                        email: usuario.email,
                        nome: usuario.nome || usuario.email.split('@')[0],
                        perfil: usuario.perfil || 'usuario',
                        admin: usuario.admin || false,
                        ativo: usuario.ativo !== false
                    };

                    await this.supabaseClient.upsert('usuarios', usuarioSupabase);
                    sucesso++;
                    this.log(`‚úÖ Usu√°rio migrado: ${usuario.email}`);

                } catch (error) {
                    erro++;
                    this.log(`‚ùå Erro usu√°rio ${usuario.email}: ${error.message}`);
                }
            }

            this.log(`üë• Usu√°rios: ${sucesso} sucessos, ${erro} erros`);
            return { sucesso, erro };

        } catch (error) {
            this.log(`‚ùå Erro na migra√ß√£o de usu√°rios: ${error.message}`);
            throw error;
        }
    }

    // üìÖ MIGRAR EVENTOS
    async migrarEventos() {
        try {
            this.log('üìÖ Migrando eventos...');
            
            if (!this.firebaseData?.dados?.eventos) {
                this.log('‚ö†Ô∏è Nenhum evento encontrado no Firebase');
                return { sucesso: 0, erro: 0 };
            }

            const eventos = this.firebaseData.dados.eventos;
            let sucesso = 0, erro = 0;

            // Buscar usu√°rios do Supabase para mapping
            const usuariosSupabase = await this.supabaseClient.buscar('usuarios');
            const emailToId = {};
            usuariosSupabase.forEach(u => emailToId[u.email] = u.id);

            for (const [id, evento] of Object.entries(eventos)) {
                try {
                    if (!evento.titulo) {
                        this.log(`‚ö†Ô∏è Evento ${id} sem t√≠tulo - ignorado`);
                        continue;
                    }

                    const eventoSupabase = {
                        titulo: evento.titulo,
                        descricao: evento.descricao || null,
                        data: this.formatarData(evento.data),
                        hora_inicio: evento.horaInicio || evento.hora_inicio || null,
                        hora_fim: evento.horaFim || evento.hora_fim || null,
                        local: evento.local || null,
                        categoria: evento.categoria || 'geral',
                        prioridade: evento.prioridade || 'media',
                        status: evento.status || 'agendado',
                        visibilidade: evento.visibilidade || 'publica',
                        cor: evento.cor || 'blue',
                        criado_por: emailToId[evento.criadoPor] || null,
                        responsavel: emailToId[evento.responsavel] || null,
                        observacoes: evento.observacoes || null
                    };

                    await this.supabaseClient.inserir('eventos', eventoSupabase);
                    sucesso++;
                    this.log(`‚úÖ Evento migrado: ${evento.titulo}`);

                } catch (error) {
                    erro++;
                    this.log(`‚ùå Erro evento ${evento.titulo}: ${error.message}`);
                }
            }

            this.log(`üìÖ Eventos: ${sucesso} sucessos, ${erro} erros`);
            return { sucesso, erro };

        } catch (error) {
            this.log(`‚ùå Erro na migra√ß√£o de eventos: ${error.message}`);
            throw error;
        }
    }

    // üìã MIGRAR TAREFAS
    async migrarTarefas() {
        try {
            this.log('üìã Migrando tarefas...');
            
            if (!this.firebaseData?.dados?.tarefas) {
                this.log('‚ö†Ô∏è Nenhuma tarefa encontrada no Firebase');
                return { sucesso: 0, erro: 0 };
            }

            const tarefas = this.firebaseData.dados.tarefas;
            let sucesso = 0, erro = 0;

            // Buscar usu√°rios do Supabase para mapping
            const usuariosSupabase = await this.supabaseClient.buscar('usuarios');
            const emailToId = {};
            usuariosSupabase.forEach(u => emailToId[u.email] = u.id);

            for (const [id, tarefa] of Object.entries(tarefas)) {
                try {
                    if (!tarefa.titulo) {
                        this.log(`‚ö†Ô∏è Tarefa ${id} sem t√≠tulo - ignorada`);
                        continue;
                    }

                    const tarefaSupabase = {
                        titulo: tarefa.titulo,
                        descricao: tarefa.descricao || null,
                        data_inicio: this.formatarData(tarefa.dataInicio || tarefa.data_inicio),
                        data_fim: this.formatarData(tarefa.dataFim || tarefa.data_fim),
                        categoria: tarefa.categoria || 'geral',
                        prioridade: tarefa.prioridade || 'media',
                        status: tarefa.status || 'pendente',
                        progresso: tarefa.progresso || 0,
                        tipo: tarefa.tipo || 'tarefa',
                        criado_por: emailToId[tarefa.criadoPor] || null,
                        responsavel: emailToId[tarefa.responsavel] || null,
                        observacoes: tarefa.observacoes || null
                    };

                    await this.supabaseClient.inserir('tarefas', tarefaSupabase);
                    sucesso++;
                    this.log(`‚úÖ Tarefa migrada: ${tarefa.titulo}`);

                } catch (error) {
                    erro++;
                    this.log(`‚ùå Erro tarefa ${tarefa.titulo}: ${error.message}`);
                }
            }

            this.log(`üìã Tarefas: ${sucesso} sucessos, ${erro} erros`);
            return { sucesso, erro };

        } catch (error) {
            this.log(`‚ùå Erro na migra√ß√£o de tarefas: ${error.message}`);
            throw error;
        }
    }

    // üîÑ MIGRA√á√ÉO COMPLETA
    async executarMigracaoCompleta() {
        try {
            console.log('üöÄ INICIANDO MIGRA√á√ÉO COMPLETA FIREBASE ‚Üí SUPABASE');
            this.status = 'migrando';
            this.logMigracao = [];

            // 1. Inicializar
            const inicializado = await this.inicializar();
            if (!inicializado) {
                throw new Error('Falha na inicializa√ß√£o');
            }

            // 2. Backup Firebase
            await this.backupFirebase();

            // 3. Migrar dados
            const resultadoUsuarios = await this.migrarUsuarios();
            const resultadoEventos = await this.migrarEventos();
            const resultadoTarefas = await this.migrarTarefas();

            // 4. Resumo
            const resumo = {
                usuarios: resultadoUsuarios,
                eventos: resultadoEventos,
                tarefas: resultadoTarefas,
                totalSucesso: resultadoUsuarios.sucesso + resultadoEventos.sucesso + resultadoTarefas.sucesso,
                totalErro: resultadoUsuarios.erro + resultadoEventos.erro + resultadoTarefas.erro
            };

            this.log('üéâ MIGRA√á√ÉO COMPLETA FINALIZADA!');
            this.log(`üìä RESUMO: ${resumo.totalSucesso} sucessos, ${resumo.totalErro} erros`);
            
            this.status = 'concluido';
            
            // Salvar log da migra√ß√£o
            localStorage.setItem('log_migracao_supabase', JSON.stringify(this.logMigracao));
            
            return resumo;

        } catch (error) {
            this.log(`‚ùå ERRO NA MIGRA√á√ÉO: ${error.message}`);
            this.status = 'erro';
            throw error;
        }
    }

    // üìä VALIDAR MIGRA√á√ÉO
    async validarMigracao() {
        try {
            this.log('üîç Validando migra√ß√£o...');

            const stats = await this.supabaseClient.obterEstatisticas();
            
            this.log(`üìä DADOS MIGRADOS:`);
            this.log(`üë• Usu√°rios: ${stats.usuarios}`);
            this.log(`üìÖ Eventos: ${stats.eventos}`);
            this.log(`üìã Tarefas: ${stats.tarefas}`);

            return stats;

        } catch (error) {
            this.log(`‚ùå Erro na valida√ß√£o: ${error.message}`);
            throw error;
        }
    }

    // üõ†Ô∏è M√âTODOS AUXILIARES
    formatarData(data) {
        if (!data) return null;
        
        // Se j√° est√° no formato YYYY-MM-DD
        if (typeof data === 'string' && data.match(/^\d{4}-\d{2}-\d{2}$/)) {
            return data;
        }

        // Converter outros formatos
        try {
            const date = new Date(data);
            return date.toISOString().split('T')[0];
        } catch {
            return null;
        }
    }

    log(mensagem) {
        const timestamp = new Date().toISOString();
        const logEntry = `[${timestamp}] ${mensagem}`;
        this.logMigracao.push(logEntry);
        console.log(logEntry);
    }

    // üìã RELAT√ìRIO COMPLETO
    gerarRelatorio() {
        return {
            status: this.status,
            log: this.logMigracao,
            timestamp: Date.now()
        };
    }
}

// üöÄ INICIALIZA√á√ÉO
const migracaoSupabase = new MigracaoSupabase();

// Expor globalmente
window.migracaoSupabase = migracaoSupabase;
window.MigracaoSupabase = MigracaoSupabase;

// üß™ FUN√á√ïES DE TESTE E EXECU√á√ÉO
window.executarMigracaoCompleta = () => migracaoSupabase.executarMigracaoCompleta();
window.backupFirebase = () => migracaoSupabase.backupFirebase();
window.validarMigracaoSupabase = () => migracaoSupabase.validarMigracao();
